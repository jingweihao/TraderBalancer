package com.balancerLL;

public class recordCategory 
{
	private String category;
	private int num;
	
	public recordCategory(String category, int num)
	{
		this.category = category;
		this.num = num;
	}
	
	public String getCategory()
	{
		return category;
	}
	
	public int getNum()
	{
		return num;
	}
}
